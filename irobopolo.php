        <p><h2>Problem Statement</h2></p><br><br>
    <p><h3>The aim of the participating team is to design a robot which is able to pick the ball and place it into the pit by crossing steps.</h3></p>
    <br><br>
    <p><h2>Arena</h2></p><br><br>
    <p><h3><p><b>1.</b>The arena is a square of 120cm x 120cm divided into 4 zones.
        <br>&nbsp;&nbsp;Zone 1: Starting Zone<br>&nbsp;&nbsp;Zone 2: Step Zone<br>&nbsp;&nbsp;Zone 3: Intermediate Zone <br>&nbsp;&nbsp; Zone 4: Pit Zone<br>
        <br></p>
    <p><b>2. </b>Zone 1 and Zone 3 are of same height whereas Zone 4 and Zone 5 are 7cm above zones 1 and 3.</p></h3>
    <h3><p><b>3. </b>Starting point is at one end of starting zone of size 25cm x 25cm.</p>
    <p><b>4. </b>10 balls were placed at random in the starting zone.</p>
    <p><b>5.</b>The robot has to pick the ball and put them into the pit one by one by passing through different zones.</p></h3>

      <p><h2>Rules</h2></p><br>
    <h3> <p><b>1. </b>Maximum team size: 3 members per team.</p>
        <p><b>2. </b>Time: 3 minutes will be provided to each robot.</p>
        <p><b>3. </b>The robot must not cause any damage to the arena. In event of any damage, the team would be liable for disqualification.</p>
        <p><b>4. </b>At any point of the match, participant cannot touch their robot</p>
        <p><b>5. </b>Maximum two time outs of about 60 seconds will be allowed, points shall be deducted for doing so.</p>
        <p><b>6. </b>A BOT will be declared ‘KNOCKED-OUT’ if it is unable to travel a distance of 2 inches in 30 seconds.</p>
        <p><b>7. </b>The participants will be provided with 220 Volts, 50Hz standard AC supply. Participants will have to themselves arrange for any other power supply.</p>
        <p><b>8. </b>Teams cannot tinker their bots on the arena.</p>
         <p><b>9. </b>Size of robot do not exceed maximum size limit any time during commencement of event.</p>
         <p><b>10. </b>The decision of ROBOTIX team is final and binding.</p>

        </h3><br><br>


       <p><h2>Robot Specification</h2></p><br>
    <h3> <p><b>1. </b>The robot(s) may be of wired or non-wired type but should be manually controlled.</p>
        <p><b>2. </b>Robot(s) do not exceed 25cm x 25cm  at any time during commencement of event.</p>
        <p><b>3. </b>In case of wired robots, wires should remain slacked.</p>
        <p><b>4. </b>Wires should be properly insulated.</p>
        <p><b>5. </b>Weights of robot do not exceed 5kg.</p>
        <p><b>6. </b>Motors do not exceed 300rpm rating. </p>
        <p><b>7. </b>Ac motors are not allowed.</p>
        <p><b>8. </b>Dc voltage does not exceed 12volts at any point.</p>
         <p><b>9. </b>Use of Lego kits and readymade bases is strictly prohibited and will lead to disqualification of team. Readymade gear boxes may be used.</p>
         <p><b>10. </b>In case of radio controlled robots, the ROBOTIX team will not be responsible for any interference. Participants will have to tune it into suitable frequency of their own.</p>

        </h3><br><br>


         <p><h2>Scoring</h2></p><br>
    <h3> <p><b>1. </b>For every ball placed in the pit, 30 points will be awarded.</p>
        <p><b>2. </b>For crossing one zone, 10 points  will be awarded i.e. if a robot places 1 ball in the pit then it gets  total 60 points(30 for putting ball into pit,30 for crossing 3 zones, starting zone not included).</p>


        <br><br><br><p><b>3. </b>Team Score = (30 x No. of balls placed in pit) + (10 x No. of zones crossed) – (50 x No. of restarts) – Penalty</p>
        <br><br><br><p><b>4. </b>Penalty: 100,Ball falls after picking in zone 2&3.
